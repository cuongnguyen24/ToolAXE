function Invoke-ServerRoleMemberExists {
    param(
        [string]$RoleName,
        [string]$LoginName
    )

    $role = Escape-SqlLiteral $RoleName
    $login = Escape-SqlLiteral $LoginName
    $connectionString = "Server=localhost;Database=master;Integrated Security=True;TrustServerCertificate=True;"
    $connection = New-Object System.Data.SqlClient.SqlConnection($connectionString)
    $command = $connection.CreateCommand()
    $command.CommandText = "SELECT ISNULL(IS_SRVROLEMEMBER(N'$role', N'$login'), 0);"

    try {
        $connection.Open()
        return ([int]$command.ExecuteScalar()) -eq 1
    }
    finally {
        $connection.Dispose()
    }
}

